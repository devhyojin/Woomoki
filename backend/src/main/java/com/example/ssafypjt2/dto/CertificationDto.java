package com.example.ssafypjt2.dto;

public class CertificationDto {

	public int id;
	public int cng_id;
	public int user_id;
	public String nickname;
	public String title;
	public String img;
	public String content;
	public String create_date;
	public int result;
	public int like_cnt;


	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getCng_id() {
		return cng_id;
	}

	public void setCng_id(int cng_id) {
		this.cng_id = cng_id;
	}

	public int getUser_id() {
		return user_id;
	}

	public void setUser_id(int user_id) {
		this.user_id = user_id;
	}

	public String getNickname() {
		return nickname;
	}

	public void setNickname(String nickname) {
		this.nickname = nickname;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getImg() {
		return img;
	}

	public void setImg(String img) {
		this.img = img;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getCreate_date() {
		return create_date;
	}

	public void setCreate_date(String create_date) {
		this.create_date = create_date;
	}

	public int getResult() {
		return result;
	}

	public void setResult(int result) {
		this.result = result;
	}

	public int getLike_cnt() {
		return like_cnt;
	}

	public void setLike_cnt(int like_cnt) {
		this.like_cnt = like_cnt;
	}

	@Override
	public String toString() {
		return "CertificationDto [id=" + id + ", cng_id=" + cng_id + ", user_id=" + user_id + ", nickname=" + nickname
				+ ", title=" + title + ", img=" + img + ", content=" + content + ", create_date=" + create_date
				+ ", result=" + result + ", like_cnt=" + like_cnt + "]";
	}




}